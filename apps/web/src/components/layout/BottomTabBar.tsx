/**
 * BottomTabBar — mobile navigation bar fixed at the bottom of the screen.
 *
 * - Visible only on viewports < md via `flex md:hidden`
 * - All interactive targets are at least 44 × 44 px (min-h-[44px] min-w-[44px])
 * - Active tab uses the accent color (#6366f1); inactive tabs use muted gray
 */
export default function BottomTabBar() {
  const pathname = usePathname();

  return (
    <nav
      className="flex md:hidden fixed bottom-0 left-0 right-0 z-50 border-t border-gray-200 bg-white"
      aria-label="Mobile navigation"
    >
      {tabs.map(({ label, href, icon: Icon, activeIcon: ActiveIcon }) => {
        const isActive =
          href === '/' ? pathname === '/' : pathname.startsWith(href);

        return (
          <Link
            key={href}
            href={href}
            aria-current={isActive ? 'page' : undefined}
            className="flex flex-1 flex-col items-center justify-center min-h-[44px] min-w-[44px] py-2 gap-0.5 text-xs font-medium transition-colors"
          >
            {isActive ? (
              <ActiveIcon className="h-6 w-6 text-[#6366f1]" />
            ) : (
              <Icon className="h-6 w-6 text-gray-500" />
            )}
            <span className={isActive ? 'text-[#6366f1]' : 'text-gray-500'}>{label}</span>
          </Link>
        );
      })}
    </nav>
  );
}
